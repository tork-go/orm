package cli

import (
	"bytes"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"sort"

	"github.com/tork-go/orm/gen/codegen"
)

// generatedHeader is the prefix every file this tool writes begins
// with. Stale file cleanup checks for it before deleting anything, so
// a handwritten file that happens to be named like a generated one is
// never removed.
var generatedHeader = []byte("// Code generated by tork. DO NOT EDIT.")

func runGenerate(out, errOut io.Writer, cfg Config) int {
	schema, files, diags, err := loadSchema(cfg.SchemaDir)
	if err != nil {
		fmt.Fprintln(errOut, "generate:", err)
		return 1
	}
	fmt.Fprintf(out, "Parsing %s (%d %s)\n", cfg.SchemaDir, len(files), plural(len(files), "file"))
	if report(errOut, diags) {
		return 1
	}

	generated, err := codegen.Generate(schema, codegen.Options{Package: cfg.Package})
	if err != nil {
		fmt.Fprintln(errOut, "generate:", err)
		return 1
	}
	if err := os.MkdirAll(cfg.OutDir, 0o755); err != nil {
		fmt.Fprintf(errOut, "generate: gen: creating output directory %q: %v\n", cfg.OutDir, err)
		return 1
	}

	written := map[string]bool{}
	for _, f := range generated {
		path := filepath.Join(cfg.OutDir, f.Name)
		if err := os.WriteFile(path, f.Content, 0o644); err != nil {
			fmt.Fprintf(errOut, "generate: gen: writing %q: %v\n", path, err)
			return 1
		}
		written[f.Name] = true
		fmt.Fprintf(out, "Generated %s\n", path)
	}

	removed, err := removeStale(cfg.OutDir, written)
	if err != nil {
		fmt.Fprintln(errOut, "generate:", err)
		return 1
	}
	for _, name := range removed {
		fmt.Fprintf(out, "Removed %s\n", filepath.Join(cfg.OutDir, name))
	}
	return 0
}

// removeStale deletes the .gen.go files this run did not write, which
// is how a model deleted from the schema stops being a Go type. A file
// is only removed once its contents have been read and found to carry
// the generated header: a name is a weak claim, and deleting a user's
// own code because it matched a pattern is unforgivable.
func removeStale(dir string, written map[string]bool) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, fmt.Errorf("gen: reading output directory %q: %w", dir, err)
	}
	var removed []string
	for _, e := range entries {
		name := e.Name()
		if e.IsDir() || written[name] || !isGeneratedName(name) {
			continue
		}
		path := filepath.Join(dir, name)
		content, err := os.ReadFile(path)
		if err != nil {
			return nil, fmt.Errorf("gen: reading %q: %w", path, err)
		}
		if !bytes.HasPrefix(content, generatedHeader) {
			continue
		}
		if err := os.Remove(path); err != nil {
			return nil, fmt.Errorf("gen: removing stale file %q: %w", path, err)
		}
		removed = append(removed, name)
	}
	sort.Strings(removed)
	return removed, nil
}

func isGeneratedName(name string) bool {
	return len(name) > len(".gen.go") && filepath.Ext(name) == ".go" &&
		name[len(name)-len(".gen.go"):] == ".gen.go"
}

func runCheck(out, errOut io.Writer, cfg Config) int {
	schema, files, diags, err := loadSchema(cfg.SchemaDir)
	if err != nil {
		fmt.Fprintln(errOut, "check:", err)
		return 1
	}
	if report(errOut, diags) {
		return 1
	}
	// Generation runs even though nothing is written, because the
	// checks it makes on its own (name collisions in the generated Go,
	// output that does not parse) are exactly the ones a user wants
	// before committing a schema.
	if _, err := codegen.Generate(schema, codegen.Options{Package: cfg.Package}); err != nil {
		fmt.Fprintln(errOut, "check:", err)
		return 1
	}
	fmt.Fprintf(out, "%s: %d %s, %d %s, no problems found\n",
		cfg.SchemaDir,
		len(files), plural(len(files), "file"),
		len(schema.Models), plural(len(schema.Models), "model"))
	return 0
}

// plural spells a count's noun, since every line this tool prints
// reports a count of something.
func plural(n int, word string) string {
	if n == 1 {
		return word
	}
	return word + "s"
}
