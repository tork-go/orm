// The filesystem refuses things for reasons a schema cannot express:
// a directory where a file should be, a mode that forbids the write.
// Every one of those paths reports which operation failed on which
// path, and none of them leaves a half written package behind. The
// tests below arrange each refusal deliberately.
//
// They rely on file modes actually being enforced, which is not true
// for root, so they skip there rather than failing confusingly.
func requireUnprivileged(t *testing.T) {
	t.Helper()
	if os.Geteuid() == 0 {
		t.Skip("file modes are not enforced for root")
	}
}

// chmodDir restores the mode at the end of the test, since a directory
// the harness cannot read is a directory it cannot clean up either.
func chmodDir(t *testing.T, dir string, mode os.FileMode) {
	t.Helper()
	if err := os.Chmod(dir, mode); err != nil {
		t.Fatalf("chmod %s: %v", dir, err)
	}
	t.Cleanup(func() {
		if err := os.Chmod(dir, 0o755); err != nil {
			t.Errorf("restoring the mode of %s: %v", dir, err)
		}
	})
}

func TestRunWithArgs_UnreadableSchemaFile(t *testing.T) {
	requireUnprivileged(t)
	cfg := project(t, map[string]string{"schema.tork": cliSchema})
	path := filepath.Join(cfg.SchemaDir, "schema.tork")
	if err := os.Chmod(path, 0o000); err != nil {
		t.Fatalf("chmod: %v", err)
	}
	code, _, errOut := runCLI(t, cfg)
	if code != 1 {
		t.Fatalf("exit code = %d, want 1", code)
	}
	if want := "generate: gen: reading " + quoted(path); !strings.Contains(errOut, want) {
		t.Errorf("stderr = %q, want it to contain %q", errOut, want)
	}
}

