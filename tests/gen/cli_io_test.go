// generated runs the tool once so the output directory exists and is
// populated, which is the starting point for the stale file cases.
func generated(t *testing.T) gencli.Config {
	t.Helper()
	cfg := project(t, map[string]string{"schema.tork": cliSchema})
	if code, _, errOut := runCLI(t, cfg); code != 0 {
		t.Fatalf("the first run failed: %s", errOut)
	}
	return cfg
}

func TestRunWithArgs_OutputDirectoryCannotBeListed(t *testing.T) {
	requireUnprivileged(t)
	cfg := generated(t)
	// Writable and traversable but not readable: the generated files
	// can still be rewritten, but the stale sweep cannot list them.
	chmodDir(t, cfg.OutDir, 0o300)
	code, _, errOut := runCLI(t, cfg)
	if code != 1 {
		t.Fatalf("exit code = %d, want 1", code)
	}
	if want := "generate: gen: reading output directory " + quoted(cfg.OutDir); !strings.Contains(errOut, want) {
		t.Errorf("stderr = %q, want it to contain %q", errOut, want)
	}
}

