func TestRunWithArgs_StaleFileCannotBeRemoved(t *testing.T) {
	requireUnprivileged(t)
	cfg := generated(t)
	stale := filepath.Join(cfg.OutDir, "post.gen.go")
	if err := os.WriteFile(stale, []byte("// Code generated by tork. DO NOT EDIT.\n"), 0o644); err != nil {
		t.Fatalf("writing the stale file: %v", err)
	}
	// Removing an entry needs write permission on the directory, while
	// rewriting the already existing generated files needs only write
	// permission on the files themselves, so this reaches the sweep
	// with everything before it having succeeded.
	chmodDir(t, cfg.OutDir, 0o500)
	code, _, errOut := runCLI(t, cfg)
	if code != 1 {
		t.Fatalf("exit code = %d, want 1", code)
	}
	if want := "generate: gen: removing stale file " + quoted(stale); !strings.Contains(errOut, want) {
		t.Errorf("stderr = %q, want it to contain %q", errOut, want)
	}
}

