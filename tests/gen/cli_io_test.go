// TestRun_ReadsTheProcessArguments covers the thin wrapper that Run is:
// everything else in the suite drives RunWithArgs, so this is the only
// place os.Args is proven to reach the dispatcher.
func TestRun_ReadsTheProcessArguments(t *testing.T) {
	cfg := project(t, map[string]string{"schema.tork": cliSchema})
	restore := os.Args
	os.Args = []string{"generate", "check"}
	defer func() { os.Args = restore }()

	if code := gencli.Run(cfg); code != 0 {
		t.Errorf("Run() = %d, want 0", code)
	}
	if _, err := os.Stat(cfg.OutDir); !os.IsNotExist(err) {
		t.Errorf("Run should have dispatched to check, which writes nothing")
	}
}

// quoted renders a path the way the error messages do, so an
// expectation reads like the message it is matching.
func quoted(path string) string { return `"` + path + `"` }
