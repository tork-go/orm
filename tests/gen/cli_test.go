// runCLI drives the generator's command line surface the way a shell
// would, with the streams captured. Every CLI test goes through this,
// so nothing in the suite depends on the real process's stdio.
func runCLI(t *testing.T, cfg gencli.Config, args ...string) (code int, out, errOut string) {
	t.Helper()
	var stdout, stderr bytes.Buffer
	code = gencli.RunWithArgs(args, &stdout, &stderr, cfg)
	return code, stdout.String(), stderr.String()
}

// project lays out a throwaway project: a schema directory holding the
// given files, and an output directory beside it.
func project(t *testing.T, files map[string]string) gencli.Config {
	t.Helper()
	root := t.TempDir()
	schemaDir := filepath.Join(root, "schema")
	if err := os.MkdirAll(schemaDir, 0o755); err != nil {
		t.Fatalf("creating schema directory: %v", err)
	}
	for name, src := range files {
		if err := os.WriteFile(filepath.Join(schemaDir, name), []byte(src), 0o644); err != nil {
			t.Fatalf("writing %s: %v", name, err)
		}
	}
	return gencli.Config{SchemaDir: schemaDir, OutDir: filepath.Join(root, "models"), Package: "models"}
}

func lsDir(t *testing.T, dir string) []string {
	t.Helper()
	entries, err := os.ReadDir(dir)
	if err != nil {
		t.Fatalf("reading %s: %v", dir, err)
	}
	var names []string
	for _, e := range entries {
		names = append(names, e.Name())
	}
	sort.Strings(names)
	return names
}

const cliSchema = `datasource db {
	provider = "postgres"
}

model User {
	id    Int    @id @default(autoincrement())
	email String @unique
}
`

func TestRunWithArgs_GenerateWritesTheModelsAndReportsThem(t *testing.T) {
	cfg := project(t, map[string]string{"schema.tork": cliSchema})
	code, out, errOut := runCLI(t, cfg)
	if code != 0 {
		t.Fatalf("exit code = %d, want 0 (stderr: %s)", code, errOut)
	}
	if errOut != "" {
		t.Errorf("stderr = %q, want empty", errOut)
	}
	wantLines := []string{
		"Parsing " + cfg.SchemaDir + " (1 file)",
		"Generated " + filepath.Join(cfg.OutDir, "user.gen.go"),
		"Generated " + filepath.Join(cfg.OutDir, "registry.gen.go"),
		"",
	}
	if got := strings.Split(out, "\n"); !equalLines(got, wantLines) {
		t.Errorf("stdout =\n%s\nwant =\n%s", out, strings.Join(wantLines, "\n"))
	}
	if want := []string{"registry.gen.go", "user.gen.go"}; !equalLines(lsDir(t, cfg.OutDir), want) {
		t.Errorf("output directory = %v, want %v", lsDir(t, cfg.OutDir), want)
	}
	content, err := os.ReadFile(filepath.Join(cfg.OutDir, "user.gen.go"))
	if err != nil {
		t.Fatalf("reading generated file: %v", err)
	}
	if !strings.HasPrefix(string(content), "// Code generated by tork. DO NOT EDIT.") {
		t.Errorf("generated file does not start with the header:\n%s", content)
	}
	if !strings.Contains(string(content), "package models") {
		t.Errorf("generated file is not in the configured package:\n%s", content)
	}
}

