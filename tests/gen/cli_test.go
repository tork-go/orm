func TestRunWithArgs_GenerateRemovesStaleFilesButNotHandwrittenOnes(t *testing.T) {
	cfg := project(t, map[string]string{"schema.tork": cliSchema})
	if code, _, errOut := runCLI(t, cfg); code != 0 {
		t.Fatalf("first run failed: %s", errOut)
	}

	stale := filepath.Join(cfg.OutDir, "post.gen.go")
	if err := os.WriteFile(stale, []byte("// Code generated by tork. DO NOT EDIT.\n\npackage models\n"), 0o644); err != nil {
		t.Fatalf("writing stale file: %v", err)
	}
	// Named like a generated file but not carrying the header, and so
	// not the generator's to delete.
	impostor := filepath.Join(cfg.OutDir, "helpers.gen.go")
	if err := os.WriteFile(impostor, []byte("package models\n\n// written by hand\n"), 0o644); err != nil {
		t.Fatalf("writing impostor: %v", err)
	}
	handwritten := filepath.Join(cfg.OutDir, "helpers.go")
	if err := os.WriteFile(handwritten, []byte("package models\n"), 0o644); err != nil {
		t.Fatalf("writing handwritten file: %v", err)
	}

	code, out, errOut := runCLI(t, cfg)
	if code != 0 {
		t.Fatalf("exit code = %d, want 0 (stderr: %s)", code, errOut)
	}
	if want := "Removed " + stale; !strings.Contains(out, want) {
		t.Errorf("stdout = %q, want it to contain %q", out, want)
	}
	want := []string{"helpers.gen.go", "helpers.go", "registry.gen.go", "user.gen.go"}
	if got := lsDir(t, cfg.OutDir); !equalLines(got, want) {
		t.Errorf("output directory = %v, want %v", got, want)
	}
}

