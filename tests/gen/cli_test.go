func TestRunWithArgs_FmtRewritesAndReports(t *testing.T) {
	cfg := project(t, map[string]string{"schema.tork": "model  A {\nid   Int @id\n}\n"})
	path := filepath.Join(cfg.SchemaDir, "schema.tork")

	code, out, errOut := runCLI(t, cfg, "fmt")
	if code != 0 {
		t.Fatalf("exit code = %d, want 0 (stderr: %s)", code, errOut)
	}
	if want := "Formatted " + path + "\n"; out != want {
		t.Errorf("stdout = %q, want %q", out, want)
	}
	got, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("reading formatted file: %v", err)
	}
	if want := "model A {\n\tid Int @id\n}\n"; string(got) != want {
		t.Errorf("file =\n%s\nwant =\n%s", got, want)
	}

	// A second run has nothing to do, and says so rather than staying
	// silent, since silence reads as "did it work?".
	code, out, _ = runCLI(t, cfg, "fmt")
	if code != 0 {
		t.Fatalf("exit code = %d, want 0", code)
	}
	if want := cfg.SchemaDir + ": 1 file already canonical\n"; out != want {
		t.Errorf("stdout = %q, want %q", out, want)
	}
}

