func TestRunWithArgs_UsageErrors(t *testing.T) {
	tests := []struct {
		name string
		args []string
	}{
		{"unknown subcommand", []string{"build"}},
		{"generate with arguments", []string{"generate", "extra"}},
		{"check with arguments", []string{"check", "extra"}},
		{"fmt with arguments", []string{"fmt", "extra"}},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			cfg := project(t, map[string]string{"schema.tork": cliSchema})
			code, out, errOut := runCLI(t, cfg, tt.args...)
			if code != 2 {
				t.Errorf("exit code = %d, want 2", code)
			}
			if !strings.Contains(errOut, "usage:") {
				t.Errorf("stderr = %q, want it to contain the usage text", errOut)
			}
			if out != "" {
				t.Errorf("stdout = %q, want empty", out)
			}
		})
	}
}

