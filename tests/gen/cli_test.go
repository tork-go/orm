// TestConfig_Defaults pins the defaults down through the behavior they
// produce: with no directories named, the tool looks for schema/ and
// writes to models/ in the working directory.
func TestConfig_Defaults(t *testing.T) {
	root := t.TempDir()
	if err := os.MkdirAll(filepath.Join(root, "schema"), 0o755); err != nil {
		t.Fatalf("creating schema directory: %v", err)
	}
	if err := os.WriteFile(filepath.Join(root, "schema", "s.tork"), []byte(cliSchema), 0o644); err != nil {
		t.Fatalf("writing schema: %v", err)
	}
	restore, err := os.Getwd()
	if err != nil {
		t.Fatalf("getwd: %v", err)
	}
	if err := os.Chdir(root); err != nil {
		t.Fatalf("chdir: %v", err)
	}
	defer func() {
		if err := os.Chdir(restore); err != nil {
			t.Fatalf("restoring the working directory: %v", err)
		}
	}()

	code, out, errOut := runCLI(t, gencli.Config{})
	if code != 0 {
		t.Fatalf("exit code = %d, want 0 (stderr: %s)", code, errOut)
	}
	if !strings.Contains(out, "Parsing schema (1 file)") {
		t.Errorf("stdout = %q, want it to read the default schema directory", out)
	}
	content, err := os.ReadFile(filepath.Join(root, "models", "user.gen.go"))
	if err != nil {
		t.Fatalf("reading the default output: %v", err)
	}
	if !strings.Contains(string(content), "package models") {
		t.Errorf("the default package should follow the output directory:\n%s", content)
	}
}

func equalLines(got, want []string) bool {
	if len(got) != len(want) {
		return false
	}
	for i := range got {
		if got[i] != want[i] {
			return false
		}
	}
	return true
}
