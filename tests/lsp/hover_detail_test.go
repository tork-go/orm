// Completion for through: offers models, because that is what a join
// model reference is.
func TestCompletion_ThroughOffersModels(t *testing.T) {
	c := start(t)
	c.initialize()
	uri := workspace(t, map[string]string{"schema.tork": ""})("schema.tork")
	text := richSchema + "\nmodel Later {\n\txs Doc[] @relation(\"L\", through: "
	c.open(uri, text)

	lines := strings.Split(text, "\n")
	var items []lsp.CompletionItem
	c.call("textDocument/completion", positionAt(uri, len(lines)-1, len(lines[len(lines)-1])), &items)
	if got := labels(items); !contains(got, "DocLabel") {
		t.Errorf("completion is missing the join model; got %v", got)
	}
}

// The model named after through: is a type reference like any other,
// so it explains itself and can be jumped to.
func TestFeatures_OnTheThroughModel(t *testing.T) {
	c := start(t)
	c.initialize()
	uri := workspace(t, map[string]string{"schema.tork": ""})("schema.tork")
	c.open(uri, richSchema)

	got := hoverAt(t, c, uri, 29, 55)
	for _, want := range []string{"model DocLabel", "Table doc_labels"} {
		if !strings.Contains(got, want) {
			t.Errorf("hover is missing %q:\n%s", want, got)
		}
	}

	var locations []lsp.Location
	c.call("textDocument/definition", positionAt(uri, 29, 55), &locations)
	if len(locations) != 1 {
		t.Fatalf("locations = %+v, want the join model", locations)
	}
	if locations[0].Range.Start.Line != 51 {
		t.Errorf("line = %d, want 51", locations[0].Range.Start.Line)
	}
}
