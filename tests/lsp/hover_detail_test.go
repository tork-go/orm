// richSchema exercises every shape a hover has something different to
// say about, so the descriptions can be checked in one session rather
// than a workspace per case.
const richSchema = `datasource db {
	provider = "postgres"
}

// Editorial state.
enum Status {
	draft
	published

	@@map("doc_status")
}

model Doc {
	id        Int       @id @default(autoincrement())
	title     String    @db.VarChar(120)
	price     Decimal   @db.Numeric(10, 2) @default(0)
	rate      Float     @default(1.5)
	live      Boolean   @default(true)
	key       Uuid      @unique @default(uuid())
	nonce     String    @default(go("myapp/ids.New"))
	tags      String[]
	scores    Int[]?
	meta      Json      @go.type("myapp/models.Meta")
	local     Json?     @go.type("Meta")
	status    Status    @default(draft)
	slug      String    @index
	deletedAt DateTime? @softDelete
	authorId  Int
	author    Author    @relation("DocAuthor", fields: [authorId], references: [id], onDelete: Cascade)
	labels    Label[]   @relation("DocLabels", through: DocLabel)

	@@check("price >= 0")
}

model Author {
	id      Int     @id @default(autoincrement())
	docs    Doc[]   @relation("DocAuthor")
	profile Profile @relation("AuthorProfile")
}

model Profile {
	id       Int    @id @default(autoincrement())
	authorId Int    @unique
	author   Author @relation("AuthorProfile", fields: [authorId], references: [id])
}

model Label {
	id   Int   @id @default(autoincrement())
	docs Doc[] @relation("DocLabels", through: DocLabel)
}

model DocLabel {
	docId   Int
	labelId Int
	doc     Doc   @relation(fields: [docId], references: [id])
	label   Label @relation(fields: [labelId], references: [id])

	@@id([docId, labelId])
}

model Composite {
	a Int
	b Int

	@@id([a, b])
}
`

// hoverAt asks for a hover and fails when there is none, since every
// case below is on something the server should have words for.
func hoverAt(t *testing.T, c *client, uri string, line, col int) string {
	t.Helper()
	var hover *lsp.Hover
	c.call("textDocument/hover", positionAt(uri, line, col), &hover)
	if hover == nil {
		t.Fatalf("no hover at %d:%d", line, col)
	}
	return hover.Contents.Value
}

func TestHover_DescribesEveryFieldShape(t *testing.T) {
	c := start(t)
	c.initialize()
	uri := workspace(t, map[string]string{"schema.tork": ""})("schema.tork")
	c.open(uri, richSchema)

	tests := []struct {
		name string
		line int
		col  int
		want []string
	}{
		{"an identity key", 13, 3, []string{"id Int", "`int`", "primary key", "generated identity"}},
		{"a sized string", 14, 3, []string{"title String", "VARCHAR", "Column `title`"}},
		{"a numeric column", 15, 3, []string{"price Decimal", "decimal.Decimal", "NUMERIC", "default `0`"}},
		{"a float default", 16, 3, []string{"rate Float", "float32", "default `1.5`"}},
		{"a boolean default", 17, 3, []string{"live Boolean", "bool", "default `TRUE`"}},
		{"a client generated uuid", 18, 3, []string{"key Uuid", "uuid.UUID", "unique", "generated by github.com/google/uuid.New"}},
		{"a client generated function", 19, 3, []string{"nonce String", "generated by myapp/ids.New"}},
		{"a list", 20, 3, []string{"tags String[]", "[]string"}},
		{"a nullable list", 21, 3, []string{"scores Int[]?", "*[]int", "nullable"}},
		{"a json column with an imported type", 22, 3, []string{"meta Json", "myapp/models.Meta"}},
		{"a json column with a local type", 23, 3, []string{"local Json?", "*Meta", "nullable"}},
		{"an enum column", 24, 3, []string{"status Status", "`string`", "default `'draft'`"}},
		{"an indexed column", 25, 3, []string{"slug String", "indexed"}},
		{"a soft delete marker", 26, 3, []string{"deletedAt DateTime?", "time.Time", "soft delete marker"}},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := hoverAt(t, c, uri, tt.line, tt.col)
			for _, want := range tt.want {
				if !strings.Contains(got, want) {
					t.Errorf("hover is missing %q:\n%s", want, got)
				}
			}
		})
	}
}

